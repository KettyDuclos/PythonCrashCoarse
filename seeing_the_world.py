places = ['Paris', 'Italy', 'Santorini', "Japan", 'Spain']

print('Original order:')
print(places)

print('Sorted list:')
sorted = sorted(places)
print(sorted)

print('Original list still intact:')
print(places)

print('Reverse order:')
places.reverse()
print(places)

places.reverse()
print(places)

places.sort()
print(places)

places.reverse()
print(places)

length = len(places)
print(length)






